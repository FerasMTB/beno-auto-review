export type I18nKey = keyof typeof translations.en;

const normalizeLanguage = (preferredLanguage?: string | null): Locale => {
  if (!preferredLanguage) {
    return "en";
  }
  const normalized = preferredLanguage.trim().toLowerCase();
  if (normalized.includes("arabic") || normalized.startsWith("ar")) {
    return "ar";
  }
  if (normalized.includes("german") || normalized.startsWith("de")) {
    return "de";
  }
  if (
    normalized.includes("french") ||
    normalized.includes("france") ||
    normalized.startsWith("fr")
  ) {
    return "fr";
  }
  if (
    normalized.includes("spanish") ||
    normalized.includes("esp") ||
    normalized.startsWith("es")
  ) {
    return "es";
  }
  return "en";
};

export const getI18n = (preferredLanguage?: string | null) => {
  const locale = normalizeLanguage(preferredLanguage);
  const t = (key: keyof typeof translations.en) =>
    translations[locale][key] ?? translations.en[key] ?? String(key);
  const dir = locale === "ar" ? "rtl" : "ltr";
  return { locale, dir, t };
};
